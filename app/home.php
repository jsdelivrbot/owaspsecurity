    <h2> Wprowadzenie</h2>
    <p>
        Głównym założeniem aplikacji internetowych jest interakcja pomiędzy użytkownikiem a serwerem aplikacji. Oznacza to, iż terminem aplikacji internetowej nazywany jest pro-gram komputerowy, który do realizacji swoich funkcji wymaga dostępu do Internetu, gdzie interfejs użytkownika i komunikacja z użytkownikiem odbywa się dzięki pomocy przeglądarki internetowej. Występuje tutaj architektura klient-serwer, przez co rozumia-ne jest, że realizacja różnych zapytań realizowana jest za pomocą kodu aplikacji umiesz-czonego na serwerze zapewniając użytkownikowi obsługę jego żądania. 
    </p>
    <p>
        Aplikacje internetowe współcześnie w Internecie możemy znaleźć praktycznie w każdej kategorii tematycznej. Przykładem aplikacji może być zarówno prosty sklep internetowy, serwis aukcyjny, blog osobisty jak i również korporacyjne serwisy bankowe i finansowe. Każdy serwis umożliwiający ingerencję w działanie strony uznawany jest zatem za taką aplikację.
    </p>
    <p>
        Obecnie dla większości przedsiębiorstw aplikacja jest jednym z ważniejszych zasobów infrastruktury danej firmy. Dzięki nim zapewniany jest kontakt z klientami, czy innymi partnerami biznesowymi. Zazwyczaj także przechowują wiele pożytecznych użytkowni-kom informacji, automatyzują dynamicznie niektóre rozwiązania oraz pomagają zarzą-dzanie firmom. Korzystanie z aplikacji internetowej jest szczególnie wygodne, gdyż nie ma potrzeby instalować jej na każdym urządzeniu, gdyż dostęp do niej jest dostępny dla każdego urządzenia podłączonego do Internetu i posiadającego dowolną przeglądarkę internetową.  
    </p>
    <p>
        Internetowe aplikacje mogą posiadać jednak luki w swoich zabezpieczeniach, poprzez które narażona zostaje nie tylko cała organizacja, jak i również pojedynczy użytkownik, który jest klientem danego serwisu. Jednym z najważniejszych elementów procesu two-rzenia aplikacji jest zatem bezpieczeństwo. Szczególnie ważne jest podczas systemów przechowujących poufne wrażliwe dane. 

    </p>
    <p>
        Programiści jednak popełniają wiele typowych błędów. Często wynika to z niewiedzy czy przeoczenia danej podatności, przez co atakujący może uzyskać nieautoryzowany dostęp do serwisu. Internetowi włamywacze próbują dokonać ataku na aplikacje, wykorzystując przy tym różne metody. Ich celem jest doprowadzenie do wystąpienia błędów, czy innych sytuacji w którym uda im się wykorzystać serwis do własnych celów. Dlatego bardzo ważne zatem jest by zaprojektowanie i zaprogramowanie aplikacji było zrealizowane w taki sposób, aby nie zawierały podatności na błędy bezpieczeństwa.
    </p>

